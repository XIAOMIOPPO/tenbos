package com.yidu.tencloud.bean.taketosend.pack;

import java.math.BigDecimal;
import java.util.Date;

public class Appearance {

	private String workSheetNo;
	private int productAmount;
	private BigDecimal actualWeight;
	private	int billingQuantity;
	private String size;
	private int packingCharges;
	private String actualPacking;
	private String drawerName;
	private Date drawerTime;
	
	public String getWorkSheetNo() {
		return workSheetNo;
	}
	public int getProductAmount() {
		return productAmount;
	}
	public BigDecimal getActualWeight() {
		return actualWeight;
	}
	public int getBillingQuantity() {
		return billingQuantity;
	}
	public String getSize() {
		return size;
	}
	public int getPackingCharges() {
		return packingCharges;
	}
	public String getActualPacking() {
		return actualPacking;
	}
	public String getDrawerName() {
		return drawerName;
	}
	public Date getDrawerTime() {
		return drawerTime;
	}
	public void setWorkSheetNo(String workSheetNo) {
		this.workSheetNo = workSheetNo;
	}
	public void setProductAmount(int productAmount) {
		this.productAmount = productAmount;
	}
	public void setActualWeight(BigDecimal actualWeight) {
		this.actualWeight = actualWeight;
	}
	public void setBillingQuantity(int billingQuantity) {
		this.billingQuantity = billingQuantity;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public void setPackingCharges(int packingCharges) {
		this.packingCharges = packingCharges;
	}
	public void setActualPacking(String actualPacking) {
		this.actualPacking = actualPacking;
	}
	public void setDrawerName(String drawerName) {
		this.drawerName = drawerName;
	}
	public void setDrawerTime(Date drawerTime) {
		this.drawerTime = drawerTime;
	}
	@Override
	public String toString() {
		return "Appearance [workSheetNo=" + workSheetNo + ", productAmount=" + productAmount + ", actualWeight="
				+ actualWeight + ", billingQuantity=" + billingQuantity + ", size=" + size + ", packingCharges="
				+ packingCharges + ", actualPacking=" + actualPacking + ", drawerName=" + drawerName + ", drawerTime="
				+ drawerTime + "]";
	}
	
	
	
	
}
