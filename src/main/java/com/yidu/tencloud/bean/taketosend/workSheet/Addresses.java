package com.yidu.tencloud.bean.taketosend.workSheet;

import java.io.Serializable;
/**
 * 收件人实体类
 * @author 陈涛
 *
 */
public class Addresses implements Serializable {

	/**
	 * 收件人Id
	 */
	private String addressesId;
	/**
	 * 收件人姓名
	 */
	private String addressesName;
	/**
	 * 收件人编号
	 */
	private String addressesNo;
	/**
	 * 收件人传真
	 */
	private String addressesFax;
	/**
	 * 收件人地址
	 */
	private String addressesAddress;
	/**
	 * 收件人单位
	 */
	private String addressesCompany;
	/**
	 * 收件人电话
	 */
	private String addressesPhone;
	/**
	 * 工作单编号
	 */
	private String workSheetNo;
	
	public Addresses() {}

	public Addresses(String addressesId, String addressesName, String addressesNo, String addressesFax,
			String addressesAddress, String addressesCompany, String addressesPhone, String workSheetNo) {
		super();
		this.addressesId = addressesId;
		this.addressesName = addressesName;
		this.addressesNo = addressesNo;
		this.addressesFax = addressesFax;
		this.addressesAddress = addressesAddress;
		this.addressesCompany = addressesCompany;
		this.addressesPhone = addressesPhone;
		this.workSheetNo = workSheetNo;
	}

	public String getAddressesId() {
		return addressesId;
	}

	public void setAddressesId(String addressesId) {
		this.addressesId = addressesId;
	}

	public String getAddressesName() {
		return addressesName;
	}

	public void setAddressesName(String addressesName) {
		this.addressesName = addressesName;
	}

	public String getAddressesNo() {
		return addressesNo;
	}

	public void setAddressesNo(String addressesNo) {
		this.addressesNo = addressesNo;
	}

	public String getAddressesFax() {
		return addressesFax;
	}

	public void setAddressesFax(String addressesFax) {
		this.addressesFax = addressesFax;
	}

	public String getAddressesAddress() {
		return addressesAddress;
	}

	public void setAddressesAddress(String addressesAddress) {
		this.addressesAddress = addressesAddress;
	}

	public String getAddressesCompany() {
		return addressesCompany;
	}

	public void setAddressesCompany(String addressesCompany) {
		this.addressesCompany = addressesCompany;
	}

	public String getAddressesPhone() {
		return addressesPhone;
	}

	public void setAddressesPhone(String addressesPhone) {
		this.addressesPhone = addressesPhone;
	}

	public String getWorkSheetNo() {
		return workSheetNo;
	}

	public void setWorkSheetNo(String workSheetNo) {
		this.workSheetNo = workSheetNo;
	}

	@Override
	public String toString() {
		return "Addresses [addressesId=" + addressesId + ", addressesName=" + addressesName + ", addressesNo="
				+ addressesNo + ", addressesFax=" + addressesFax + ", addressesAddress=" + addressesAddress
				+ ", addressesCompany=" + addressesCompany + ", addressesPhone=" + addressesPhone + ", workSheetNo="
				+ workSheetNo + "]";
	}

	
}
