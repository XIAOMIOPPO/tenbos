package com.yidu.tencloud.bean.taketosend.pack;

import java.math.BigDecimal;
import java.util.Date;

/**
 * 包装实体类
 * @author 刘子轩
 * @since 2017/11/28
 */
public class Pack {
	
	/**
	 * 包装Id
	 */
	private String packageId;
	
	/**
	 * 工作单号
	 */
	private String jobListNo;
	
	/**
	 * 条码
	 */
	private String stripeCode;
	
	/**
	 * 本次重量
	 */
	private BigDecimal thisWeight;
	
	/**
	 * 操作时间
	 */
	private Date operateTime;
	
	/**
	 * 操作人Id(外键)
	 */
	private String operatingPer;
	
	/**
	 * 件数
	 */
	private Integer packagesNum;
	
	/**
	 * 已核件
	 */
	private Integer nucleatedParts;
	
	/**
	 * 核重单位
	 */
	private String nuclearWeightUnit;
	
	/**
	 * 已核重
	 */
	private BigDecimal nucleatedHeavy;
	
	/**
	 * 组织名
	 */
	private String orgName;
	
	/**
	 * 重量差
	 */
	private BigDecimal gewichtsdifferenz;
	
	/**
	 * 用户名
	 */
	private String userName;
	
	/**
	 * 入库时间
	 */
	private Date storageTime;
	
	/**
	 * 到达地 冗余字段
	 */
	private String whither;
	
	/**
	 * 原重量 冗余字段
	 */
	private BigDecimal factWeight;
	
	/**
	 * 无参数的构造方法
	 */
	public Pack() {}
	
	/**
	 * 带参数的构造方法
	 * @param packageId 包装Id
	 * @param jobListNo 工作单号
	 * @param stripeCode 条码
	 * @param thisWeight 本次重量
	 * @param operateTime 操作时间
	 * @param operatingPer 操作人Id
	 * @param packagesNum 件数
	 * @param nucleatedParts 已核件
	 * @param nuclearWeightUnit 核重单位
	 * @param nucleatedHeavy 已核重
	 * @param orgName 组织名
	 * @param gewichtsdifferenz 重量差
	 * @param userName 用户名
	 * @param storageTime 入库时间
	 * @param whither 到达地
	 * @param factWeight 原重量
	 */
	public Pack(String packageId, String jobListId, String jobListNo, String stripeCode, BigDecimal thisWeight,
			Date operateTime, String operatingPer, Integer packagesNum, Integer nucleatedParts,
			String nuclearWeightUnit, BigDecimal nucleatedHeavy, String orgName, BigDecimal gewichtsdifferenz,
			String userName, Date storageTime, String whither, BigDecimal factWeight) {
		super();
		this.packageId = packageId;
		this.jobListNo = jobListNo;
		this.stripeCode = stripeCode;
		this.thisWeight = thisWeight;
		this.operateTime = operateTime;
		this.operatingPer = operatingPer;
		this.packagesNum = packagesNum;
		this.nucleatedParts = nucleatedParts;
		this.nuclearWeightUnit = nuclearWeightUnit;
		this.nucleatedHeavy = nucleatedHeavy;
		this.orgName = orgName;
		this.gewichtsdifferenz = gewichtsdifferenz;
		this.userName = userName;
		this.storageTime = storageTime;
		this.whither = whither;
		this.factWeight = factWeight;
	}
	
	/**
	 * 获得包装Id
	 * @return
	 */
	public String getPackageId() {
		return packageId;
	}
	
	/**
	 * 为包装Id赋值
	 * @param packageId 
	 */
	public void setPackageId(String packageId) {
		this.packageId = packageId;
	}
	
	
	/**
	 * 获得工作单号
	 * @return
	 */
	public String getJobListNo() {
		return jobListNo;
	}
	
	/**
	 * 为工作单号赋值
	 * @param jobListNo
	 */
	public void setJobListNo(String jobListNo) {
		this.jobListNo = jobListNo;
	}
	
	/**
	 * 获得条码
	 * @return
	 */
	public String getStripeCode() {
		return stripeCode;
	}
	
	/**
	 * 为条码赋值
	 * @param stripeCode
	 */
	public void setStripeCode(String stripeCode) {
		this.stripeCode = stripeCode;
	}
	
	/**
	 * 获得本次重量
	 * @return
	 */
	public BigDecimal getThisWeight() {
		return thisWeight;
	}
	
	/**
	 * 为本次重量赋值
	 * @param thisWeight
	 */
	public void setThisWeight(BigDecimal thisWeight) {
		this.thisWeight = thisWeight;
	}
	
	/**
	 * 获得操作时间
	 * @return
	 */
	public Date getOperateTime() {
		return operateTime;
	}
	
	/**
	 * 为操作时间赋值
	 * @param operateTime
	 */
	public void setOperateTime(Date operateTime) {
		this.operateTime = operateTime;
	}
	
	/**
	 * 获得操作人Id
	 * @return
	 */
	public String getOperatingPer() {
		return operatingPer;
	}
	
	/**
	 * 为操作人Id赋值
	 * @param operatingPer
	 */
	public void setOperatingPer(String operatingPer) {
		this.operatingPer = operatingPer;
	}
	
	/**
	 * 获得件数
	 * @return
	 */
	public Integer getPackagesNum() {
		return packagesNum;
	}
	
	/**
	 * 为件数赋值
	 * @param packagesNum
	 */
	public void setPackagesNum(Integer packagesNum) {
		this.packagesNum = packagesNum;
	}
	
	/**
	 * 获得已核件
	 * @return
	 */
	public Integer getNucleatedParts() {
		return nucleatedParts;
	}
	
	/**
	 * 为已核件赋值
	 * @param nucleatedParts
	 */
	public void setNucleatedParts(Integer nucleatedParts) {
		this.nucleatedParts = nucleatedParts;
	}
	
	/**
	 * 获得核重单位
	 * @return
	 */
	public String getNuclearWeightUnit() {
		return nuclearWeightUnit;
	}
	
	/**
	 * 为核重单位赋值
	 * @param nuclearWeightUnit
	 */
	public void setNuclearWeightUnit(String nuclearWeightUnit) {
		this.nuclearWeightUnit = nuclearWeightUnit;
	}
	
	/**
	 * 获得已核重
	 * @return
	 */
	public BigDecimal getNucleatedHeavy() {
		return nucleatedHeavy;
	}
	
	/**
	 * 为已核重赋值
	 * @param nucleatedHeavy
	 */
	public void setNucleatedHeavy(BigDecimal nucleatedHeavy) {
		this.nucleatedHeavy = nucleatedHeavy;
	}
	
	/**
	 * 获得组织名
	 * @return
	 */
	public String getOrgName() {
		return orgName;
	}
	
	/**
	 * 为组织名赋值
	 * @param orgName
	 */
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	
	/**
	 * 获得重量差
	 * @return
	 */
	public BigDecimal getGewichtsdifferenz() {
		return gewichtsdifferenz;
	}
	
	/**
	 * 为重量差赋值
	 * @param gewichtsdifferenz
	 */
	public void setGewichtsdifferenz(BigDecimal gewichtsdifferenz) {
		this.gewichtsdifferenz = gewichtsdifferenz;
	}
	
	/**
	 * 获得用户名
	 * @return
	 */
	public String getUserName() {
		return userName;
	}
	
	/**
	 * 为用户名赋值
	 * @param userName
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	/**
	 * 获得入库时间
	 * @return
	 */
	public Date getStorageTime() {
		return storageTime;
	}
	
	/**
	 * 为入库时间赋值
	 * @param storageTime
	 */
	public void setStorageTime(Date storageTime) {
		this.storageTime = storageTime;
	}
	
	/**
	 * 获得到达地
	 * @return
	 */
	public String getWhither() {
		return whither;
	}
	
	/**
	 * 为到达地赋值
	 * @param whither
	 */
	public void setWhither(String whither) {
		this.whither = whither;
	}
	
	/**
	 * 获得原重量
	 * @return
	 */
	public BigDecimal getFactWeight() {
		return factWeight;
	}
	
	/**
	 * 为原重量赋值
	 * @param factWeight
	 */
	public void setFactWeight(BigDecimal factWeight) {
		this.factWeight = factWeight;
	}

	@Override
	public String toString() {
		return "Pack [packageId=" + packageId + ", jobListNo=" + jobListNo + ", stripeCode=" + stripeCode
				+ ", thisWeight=" + thisWeight + ", operateTime=" + operateTime + ", operatingPer=" + operatingPer
				+ ", packagesNum=" + packagesNum + ", nucleatedParts=" + nucleatedParts + ", nuclearWeightUnit="
				+ nuclearWeightUnit + ", nucleatedHeavy=" + nucleatedHeavy + ", orgName=" + orgName
				+ ", gewichtsdifferenz=" + gewichtsdifferenz + ", userName=" + userName + ", storageTime=" + storageTime
				+ ", whither=" + whither + ", factWeight=" + factWeight + "]";
	}

	
	
}
