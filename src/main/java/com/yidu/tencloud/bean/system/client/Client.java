package com.yidu.tencloud.bean.system.client;

import java.io.Serializable;

import org.springframework.stereotype.Component;
/**
 * @since 2017-11-06
 * @author 刘艺钿
 */
@Component
public class Client implements Serializable {
	private static final long serialVersionUID = 1L;
		private String clientId;
		private String clientNum;
	    private String clientName;
	    private String sex;
	    private String phoneNum;
	    private String clientType;
	    private String clientAddress;
	    private String newFrom;
		public String getClientId() {
			return clientId;
		}
		public void setClientId(String clientId) {
			this.clientId = clientId;
		}
		public String getClientNum() {
			return clientNum;
		}
		public void setClientNum(String clientNum) {
			this.clientNum = clientNum;
		}
		public String getClientName() {
			return clientName;
		}
		public void setClientName(String clientName) {
			this.clientName = clientName;
		}
		public String getSex() {
			return sex;
		}
		public void setSex(String sex) {
			this.sex = sex;
		}
		public String getPhoneNum() {
			return phoneNum;
		}
		public void setPhoneNum(String phoneNum) {
			this.phoneNum = phoneNum;
		}
		public String getClientType() {
			return clientType;
		}
		public void setClientType(String clientType) {
			this.clientType = clientType;
		}
		public String getClientAddress() {
			return clientAddress;
		}
		public void setClientAddress(String clientAddress) {
			this.clientAddress = clientAddress;
		}
		public String getNewFrom() {
			return newFrom;
		}
		public void setNewFrom(String newFrom) {
			this.newFrom = newFrom;
		}
		public static long getSerialversionuid() {
			return serialVersionUID;
		}
		public Client(String clientId, String clientNum, String clientName, String sex, String phoneNum,
				String clientType, String clientAddress, String newFrom) {
			super();
			this.clientId = clientId;
			this.clientNum = clientNum;
			this.clientName = clientName;
			this.sex = sex;
			this.phoneNum = phoneNum;
			this.clientType = clientType;
			this.clientAddress = clientAddress;
			this.newFrom = newFrom;
		}
		@Override
		public String toString() {
			return "Client [clientId=" + clientId + ", clientNum=" + clientNum + ", clientName=" + clientName + ", sex="
					+ sex + ", phoneNum=" + phoneNum + ", clientType=" + clientType + ", clientAddress=" + clientAddress
					+ ", newFrom=" + newFrom + "]";
		}
		public Client() {
			super();
		}
		
	    

		
	
	   
}
