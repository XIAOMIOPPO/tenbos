package com.yidu.tencloud.bean.route.train;

import java.util.List;

public class RLine {
	private String lineId;
	
	private String lineCode;
	
	private String StartingCity;
	
	private String StartingStation;
	
	private String arriveCity;
	
	private String arriveStation;
	
	private String LineName;
	
	private Integer distance;
	
	private String status;
	
	private List<Route> route;
	
	public RLine() {}

	public RLine(String lineId, String lineCode, String startingCity, String startingStation, String arriveCity,
			String arriveStation, String lineName, Integer distance, String status, List<Route> route) {
		super();
		this.lineId = lineId;
		this.lineCode = lineCode;
		StartingCity = startingCity;
		StartingStation = startingStation;
		this.arriveCity = arriveCity;
		this.arriveStation = arriveStation;
		LineName = lineName;
		this.distance = distance;
		this.status = status;
		this.route = route;
	}

	public String getLineId() {
		return lineId;
	}

	public void setLineId(String lineId) {
		this.lineId = lineId;
	}

	public String getLineCode() {
		return lineCode;
	}

	public void setLineCode(String lineCode) {
		this.lineCode = lineCode;
	}

	public String getStartingCity() {
		return StartingCity;
	}

	public void setStartingCity(String startingCity) {
		StartingCity = startingCity;
	}

	public String getStartingStation() {
		return StartingStation;
	}

	public void setStartingStation(String startingStation) {
		StartingStation = startingStation;
	}

	public String getArriveCity() {
		return arriveCity;
	}

	public void setArriveCity(String arriveCity) {
		this.arriveCity = arriveCity;
	}

	public String getArriveStation() {
		return arriveStation;
	}

	public void setArriveStation(String arriveStation) {
		this.arriveStation = arriveStation;
	}

	public String getLineName() {
		return LineName;
	}

	public void setLineName(String lineName) {
		LineName = lineName;
	}

	public Integer getDistance() {
		return distance;
	}

	public void setDistance(Integer distance) {
		this.distance = distance;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public List<Route> getRoute() {
		return route;
	}

	public void setRoute(List<Route> route) {
		this.route = route;
	}

	@Override
	public String toString() {
		return "RLine [lineId=" + lineId + ", lineCode=" + lineCode + ", StartingCity=" + StartingCity
				+ ", StartingStation=" + StartingStation + ", arriveCity=" + arriveCity + ", arriveStation="
				+ arriveStation + ", LineName=" + LineName + ", distance=" + distance + ", status=" + status
				+ ", route=" + route + "]";
	}

	

}
