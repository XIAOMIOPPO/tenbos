package com.yidu.tencloud.bean.route.line;

public class Line {
	
private String lineId;
	
	private String lineCode;
	
	private String startingCity;
	
	private String startingStation;
	
	private String arriveCity;
	
	private String arriveStation;
	
	private String lineName;
	
	private String distance;
	
	private String status;
	
	public Line() {}

	public Line(String lineId, String lineCode, String startingCity, String startingStation, String arriveCity,
			String arriveStation, String lineName, String distance, String status) {
		super();
		this.lineId = lineId;
		this.lineCode = lineCode;
		this.startingCity = startingCity;
		this.startingStation = startingStation;
		this.arriveCity = arriveCity;
		this.arriveStation = arriveStation;
		this.lineName = lineName;
		this.distance = distance;
		this.status = status;
	}

	public String getLineId() {
		return lineId;
	}

	public void setLineId(String lineId) {
		this.lineId = lineId;
	}

	public String getLineCode() {
		return lineCode;
	}

	public void setLineCode(String lineCode) {
		this.lineCode = lineCode;
	}

	public String getStartingCity() {
		return startingCity;
	}

	public void setStartingCity(String startingCity) {
		this.startingCity = startingCity;
	}

	public String getStartingStation() {
		return startingStation;
	}

	public void setStartingStation(String startingStation) {
		this.startingStation = startingStation;
	}

	public String getArriveCity() {
		return arriveCity;
	}

	public void setArriveCity(String arriveCity) {
		this.arriveCity = arriveCity;
	}

	public String getArriveStation() {
		return arriveStation;
	}

	public void setArriveStation(String arriveStation) {
		this.arriveStation = arriveStation;
	}

	public String getLineName() {
		return lineName;
	}

	public void setLineName(String lineName) {
		this.lineName = lineName;
	}

	public String getDistance() {
		return distance;
	}

	public void setDistance(String distance) {
		this.distance = distance;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	@Override
	public String toString() {
		return "Line [lineId=" + lineId + ", lineCode=" + lineCode + ", startingCity=" + startingCity
				+ ", startingStation=" + startingStation + ", arriveCity=" + arriveCity + ", arriveStation="
				+ arriveStation + ", lineName=" + lineName + ", distance=" + distance + ", status=" + status + "]";
	}

	
	
}
